function destacarParagrafo() {
    let paragrafo = document.getElementById('meuParagrafo');
    paragrafo.classList.add('destacado');
}

function removerDestaque() {
    let paragrafo = document.getElementById('meuParagrafo');
    paragrafo.classList.remove('destacado');
}





